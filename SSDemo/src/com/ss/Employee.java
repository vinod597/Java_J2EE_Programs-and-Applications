package com.ss;

public class Employee {
	private int employeeId;
	private String employeeName;
	private double employeeSlary;
	private double emploan;
	private double emi;
	private double exp;

	public int getEmployeeId() {
		return employeeId;
	}

	public void setEmployeeId(int employeeId) {
		this.employeeId = employeeId;
	}

	public String getEmployeeName() {
		return employeeName;
	}

	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}

	public double getEmployeeSlary() {
		return employeeSlary;
	}

	public void setEmployeeSlary(double employeeSlary) {
		this.employeeSlary = employeeSlary;
	}

	public double getEmploan() {
		return emploan;
	}

	public void setEmploan(double emploan) {
		this.emploan = emploan;
	}

	public double getEmi() {
		return emi;
	}

	public void setEmi(double emi) {
		this.emi = emi;
	}

	public double getExp() {
		return exp;
	}

	public void setExp(double exp) {
		this.exp = exp;
	}

}
